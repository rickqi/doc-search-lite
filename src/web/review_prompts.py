# ── 隐性违规判断指引（注入到合规审查 System Prompt） ──────────

REVIEW_ENHANCEMENT = """
# 隐性违规判断指引（增强版）

## Rule 116.6 — 保证性承诺：隐性保证示例
以下表述虽未直接使用"保证"字样，但构成隐性保证性承诺，应判定为违规(pass=N)：
- "盈余分配" → 暗示确定能分配到盈余（分红保险的红利分配是不确定的）
- "身价倍增" → 暗示保额确定会翻倍增值
- "锁定收益""保本""稳赚" → 暗示确定能获得收益
- "无忧""万无一失" → 暗示无任何风险
- "确保""一定""百分百" → 确定性兜底承诺

注意：以上词语出现在产品描述中时，即使带有"以合同约定为准"等限定语，
也应标记为需关注的隐性保证表述。

## Rule 116.24 — 不良用语：不当类比与恐惧营销
以下表述应判定为违规(pass=N)：
- 军事隐喻："精准制导导弹""核弹级""轰炸癌细胞""靶向打击"等
- 恐惧营销："救命""不买就晚了""一旦发生就完了""最后机会"等
- 暴力/灾难类比："毁灭性""致命""灾难性"等

## Rule 116.12 — 绝对化用语：扩展边界
以下词语应考虑标记：
- "明星产品""王者""标杆""天花板"等 — 属于变相绝对化用语
- 即使不在明确列举列表中（第一、最、顶尖、领先、极、首），
  只要是"暗示在同行业中处于最高水平"的表述，都应予以关注。

## Rule 74 — 风险提示触发条件
当材料提及以下任一功能时，必须同时提供风险提示（否则 pass=N）：
- 减保 → 必须提示"减保可能产生损失"
- 保单贷款 → 必须提示"需偿还利息，未偿还可能影响保单效力"
- 退保 → 必须提示"退保金额可能低于已交保费"
- 分红 → 必须提示"红利分配是不确定的"

## Rule 92 — 保险标识：条件分支
- 保险公司直营互联网产品：不需要标注保险中介机构全称
- 通过经纪/代理渠道销售的产品：必须标注保险中介机构全称
- 判断依据：如果材料中未出现中介机构名称且元数据未标注代理渠道，
  则视为直营，不要求标注中介机构。

## 过度审查控制
以下情况不应判定为违规：
- "在符合保险责任的情况下""以合同约定为准"等包含限定条件的表述，
  如果限定条件明确且合理，应视为合规(pass=Y)
- "明星产品""无忧"等常见营销用语，在无其他违规特征时，标记为
  "建议关注"而非直接判违规（pass=Y 但 hit_word 标注风险词）
- 对于医学/技术领域的常见比喻（如"靶向治疗""精准治疗"），
  除非明确构成军事隐喻（如"导弹""轰炸"），否则不判违规
"""

# ── 合规审查检测 ─────────────────────────────────────────────

def detect_review_mode(prompt: str) -> bool:
    """判断是否为合规模审查 prompt（含规则+格式约束）。

    检测特征：角色设定 + 审查规则 + JSON输出格式

    Returns:
        True 如果是合规审查 prompt，应直接调用 LLM 而非 Agent 搜索。
    """
    if not prompt:
        return False

    indicators = [
        "审查规则", "待审查材料", "审查材料",
        '"pass": "Y 或 N"', '"rule_id"',
        "输出格式（严格 JSON）", "输出格式",
        "# 角色设定", "审查专家",
        "pass\": \"Y",
    ]
    score = sum(1 for kw in indicators if kw in prompt)
    return score >= 3


def enhance_review_prompt(prompt: str) -> str:
    """增强合规审查 prompt：注入隐性违规示例库。

    在原始 prompt 的"审查规则"部分之后插入增强指引。
    如果检测不到适合插入的位置，则原样返回。

    Args:
        prompt: 原始合规审查 prompt

    Returns:
        增强后的 prompt
    """
    if not detect_review_mode(prompt):
        return prompt

    # 在"# 任务要求"之前插入增强指引
    insertion_point = prompt.find("# 任务要求")
    if insertion_point == -1:
        insertion_point = prompt.find("任务要求")

    if insertion_point > 0:
        return (
            prompt[:insertion_point] +
            REVIEW_ENHANCEMENT + "\n" +
            prompt[insertion_point:]
        )

    # Fallback: 在末尾追加
    return prompt + "\n" + REVIEW_ENHANCEMENT
